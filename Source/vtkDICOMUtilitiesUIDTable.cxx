const char *vtkDICOMUtilities::GetUIDName(const char *uid)
{
  const char *result = "";
  UIDTableEntry *table = GetUIDTableEntry(uid);
  if (table)
    {
    if (table->Name)
      {
      result = table->Name;
      }
    }
  return result;
}

unsigned short vtkDICOMUtilities::GetCIDFromUID(const char *uid)
{
  unsigned short result = 0;
  UIDTableEntry *table = GetUIDTableEntry(uid);
  if (table)
    {
    result = table->CID;
    }
  return result;
}
